package com.Bridgelabz.BasicCore;

public class SwapTwoNumbers {
    public static void main(String[] args) {
        int temp;
        int a = 10;
        int b = 20;

        temp = a;
        a = b;
        b = temp;
        System.out.println("after swap number  is  a= " + a);
        System.out.println(" after swap number is b= " + b);
    }
}

